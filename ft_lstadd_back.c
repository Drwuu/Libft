#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list *elem;

	if (*alst)
	{
		elem = ft_lstlast(*alst);
		elem->next = new;
	}
	else
		*alst = new;
	new->next = NULL;
}
